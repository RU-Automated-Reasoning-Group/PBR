class MaskedCategorical(Categorical):
    def __init__(self, logits, mask=None):
        self.mask = mask
        self.batch, self.nb_action = logits.size()
        if mask is None:
            super(MaskedCategorical, self).__init__(logits=logits)
        else:
            self.mask_value = torch.finfo(logits.dtype).min
            logits.masked_fill_(~self.mask, self.mask_value)
            super(MaskedCategorical, self).__init__(logits=logits)


class MLPMaskedCategoricalActor(Actor):
    def __init__(self, obs_dim, act_dim, hidden_sizes, activation):
        super().__init__()
        self.logits_net = mlp([obs_dim] + list(hidden_sizes) + [act_dim], activation)

    def _distribution(self, obs, mask=[]):

        CODE_DIM = 19

        # convert maske to vector (True/False)
        mask_vector = torch.ones((1, CODE_DIM), dtype=torch.bool)
        assert isinstance(mask, list)
        mask_vector[0, mask] = False

        logits = self.logits_net(obs)
        return MaskedCategorical(logits=logits, mask=mask_vector)

    def _log_prob_from_distribution(self, pi, act):
        return pi.log_prob(act)

    def entropy(self):
        if self.mask is None:
            return super().entropy()
        # Elementwise multiplication
        p_log_p = einsum("ij,ij->ij", self.logits, self.probs)
        # Compute the entropy with possible action only
        p_log_p = torch.where(
            self.mask,
            p_log_p,
            torch.tensor(0, dtype=p_log_p.dtype, device=p_log_p.device),
        )
        return -reduce(p_log_p, "b a -> b", "sum", b=self.batch, a=self.nb_action)


class MLPMaskedActorCritic(nn.Module):
    def __init__(self, observation_space, action_space, 
                 hidden_sizes=(64,64), activation=nn.Tanh):
        super().__init__()

        obs_dim = observation_space.shape[0]

        # policy builder depends on action space
        if isinstance(action_space, Box):
            raise NotImplementedError
        elif isinstance(action_space, Discrete):
            self.pi = MLPMaskedCategoricalActor(obs_dim, action_space.n, hidden_sizes, activation)

        # build value function
